# Ask the user for their name
name = input("What is your name? ")

# Ask the user for their age
age = int(input("How old are you? "))

# Print out a greeting message with their name
print("Hello, " + name + "!")

# Calculate how old they will be in 5 years
future_age = age + 5

# Print out a message telling them how old they will be in 5 years
print("In 5 years, you will be " + str(future_age) + " years old.")
